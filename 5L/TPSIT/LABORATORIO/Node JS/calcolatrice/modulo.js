//Funzione SOMMA
export const somma=(num1, num2) => {
    let totale = num1 + num2
    return (totale)
}

//Funzione DIFFERENZA
export const differenza=(num1, num2) => {
    let totale = num1 - num2
    return (totale)
}

//Funzione MOLTIPLICAZIONE
export const moltiplicazione=(num1, num2) => {
    let totale = num1 * num2
    return (totale)
}

//Funzione DIVISIONE
export const divisione=(num1, num2) => {
    let totale = num1 / num2
    return (totale)
}

/*
Per esportare le mie funzioni inserisco "export" prima di "const" dove dichiaro le funzioni
*/